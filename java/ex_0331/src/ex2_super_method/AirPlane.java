package ex2_super_method;

public class AirPlane {
	
	//"착륙합니다"라고 출력하는 land()메서드
	
	//"일반 비행합니다"라고 출력하는 fly()메서드
	
	//"이륙합니다"라고 출력하는 takeOff()메서드
	
	public void land() {
		System.out.println("착륙합니다");
	}
	
	public void fly() {
		System.out.println("일반 비행합니다");
	}
	
	public void takeOff() {
		System.out.println("이륙합니다");
	}
}
